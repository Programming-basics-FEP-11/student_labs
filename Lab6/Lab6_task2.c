#include <stdio.h>

int main() {
    int my_number, sum = 0;  // Змінні для порядкового номера та суми

    // Введення порядкового номера від користувача
    printf("Введіть ваш порядковий номер у журналі: ");
    scanf("%d", &my_number);

    // Цикл від 1 до 100 для підрахунку суми
    for (int i = 1; i <= 100; i++) {
        if (i != my_number && i != 33) {
            sum += i;  // Додаємо число до суми, якщо воно не дорівнює my_number і 33
        }
    }

    // Виведення результату
    printf("Сума перших 100 чисел за винятком %d і 33 дорівнює: %d\n", my_number, sum);

    return 0;
}
