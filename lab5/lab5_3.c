#include <stdio.h>
#include <math.h>

int main() {
    double a, b, c;

    // Введення довжин сторін трикутника
    printf("Введіть довжини трьох сторін трикутника: ");
    scanf("%lf %lf %lf", &a, &b, &c);

    // Перевірка, чи може трикутник існувати
    if (a + b > c && a + c > b && b + c > a) {
        // Визначення типу трикутника за сторонами
        if (a == b && b == c) {
            printf("Трикутник є рівностороннім (правильним).\n");
        } else if (a == b || b == c || a == c) {
            printf("Трикутник є рівнобедреним.\n");
        } else {
            printf("Трикутник є різностороннім.\n");
        }

        // Визначення типу трикутника за кутами
        // Використовуємо теорему Піфагора для перевірки типу кута
        double a2 = pow(a, 2);
        double b2 = pow(b, 2);
        double c2 = pow(c, 2);

        // Перевірка на прямий кут
        if (fabs(a2 + b2 - c2) < 0.0001 || fabs(a2 + c2 - b2) < 0.0001 || fabs(b2 + c2 - a2) < 0.0001) {
            printf("Трикутник є прямокутним.\n");
        } 
        // Перевірка на тупий кут
        else if (a2 + b2 < c2 || a2 + c2 < b2 || b2 + c2 < a2) {
            printf("Трикутник є тупокутним.\n");
        } 
        // Якщо жодна з попередніх умов не виконана, трикутник є гострокутним
        else {
            printf("Трикутник є гострокутним.\n");
        }
    } else {
        printf("Такий трикутник не може існувати.\n");
    }

    return 0;
}
