#include <stdio.h>

int main() {
    int N;

    // Запит у користувача ціле число N у діапазоні [7; 12]
    do {
        printf("Введіть ціле число N (7 <= N <= 12): ");
        scanf("%d", &N);
    } while (N < 7 || N > 12);  // Повторюємо запит, поки N не в діапазоні

    double numbers[N];  // Масив для зберігання N дійсних чисел
    double sum = 0.0;   // Змінна для зберігання суми
    double min, max;    // Змінні для зберігання мінімального і максимального значення

    // Введення N дійсних чисел
    for (int i = 0; i < N; i++) {
        printf("Введіть дійсне число %d: ", i + 1);
        scanf("%lf", &numbers[i]);  // Читання дійсного числа
        sum += numbers[i];           // Додавання числа до суми

        // Встановлення мінімального і максимального значення
        if (i == 0) {  // Якщо це перше число
            min = numbers[i];
            max = numbers[i];
        } else {
            if (numbers[i] < min) {
                min = numbers[i];  // Оновлення мінімального значення
            }
            if (numbers[i] > max) {
                max = numbers[i];  // Оновлення максимального значення
            }
        }
    }

    // Обчислення середнього арифметичного
    double average = sum / N;

    // Виведення результатів
    printf("Сума: %.2f\n", sum);
    printf("Середнє арифметичне: %.2f\n", average);
    printf("Мінімальне значення: %.2f\n", min);
    printf("Максимальне значення: %.2f\n", max);

    return 0;
}
