#include <stdio.h>

int main() {
    int sum = 0;   // Змінна для зберігання суми
    int i = 1;     // Лічильник для перебору чисел

    while (i <= 100) {
        // Перевірка, чи число не є 6 або 33
        if (i != 6 && i != 33) {
            sum += i;  // Додавання числа до суми
        }
        i++;  // Перехід до наступного числа
    }

    printf("Сума перших 100 чисел (без 6 і 33): %d\n", sum); // Виведення результату
    return 0;
}
