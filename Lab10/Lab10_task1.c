#include <stdio.h>

// Функція для обчислення довжини рядка
int len() {
    unsigned i = 0;
    char ch;
    // Читаємо символи, поки не зустрінемо '\n'
    while ((ch = getchar()) != '\n') {
        i++;
    }
    return i; // Повертаємо кількість символів
}

int main() {
  
    printf("Введіть стрічку: ");
    int length = len(); // Викликаємо функцію для підрахунку довжини

    printf("Довжина стрічки = %d\n", length); // Виводимо результат

    return 0;
}
